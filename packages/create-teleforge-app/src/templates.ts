function botIndexTs(): string {
  return `import { startTeleforgeBot } from "teleforge";

const { stop } = await startTeleforgeBot();

process.on("SIGINT", () => {
  console.log("\n[bot] shutting down...");
  stop();
  process.exit(0);
});

process.on("SIGTERM", () => {
  console.log("\n[bot] shutting down...");
  stop();
  process.exit(0);
});
`;
}

function botRuntimeTs(): string {
  return `import { startTeleforgeBot } from "teleforge";
import { type BotRuntime } from "teleforge/bot";

export async function createDevBotRuntime(options?: { miniAppUrl?: string }): Promise<BotRuntime> {
  const { runtime } = await startTeleforgeBot({
    miniAppUrl: options?.miniAppUrl,
    previewStart: false,
    bot: {
      async sendMessage() {
        return { chat: { id: 0 }, message_id: 0, text: "" };
      },
      async setCommands() {}
    }
  });
  return runtime;
}
`;
}

function botStartFlowTs(appName: string, packageNames: PackageNames): string {
  return `import { defineFlow } from "teleforge/web";
import type { StartFlowState } from "${packageNames.types}";

export default defineFlow<StartFlowState>({
  id: "start",
  initialStep: "home",
  state: {
    visited: false
  },
  bot: {
    command: {
      buttonText: "Open ${appName}",
      command: "start",
      description: "Start the Mini App",
      text: "Welcome to ${appName}. Launch the Mini App to continue."
    }
  },
  miniApp: {
    launchModes: ["inline", "compact", "fullscreen"],
    route: "/"
  },
  steps: {
    home: {
      screen: "home",
      type: "miniapp"
    }
  }
});
`;
}

function botStartTestTs(appName: string): string {
  return `import assert from "node:assert/strict";
import test from "node:test";

import startFlow from "../src/flows/start.flow.ts";

test("start flow declares the bot entry command", () => {
  assert.equal(startFlow.bot?.command?.command, "start");
  assert.equal(startFlow.bot?.command?.buttonText, "Open ${appName}");
  assert.match(
    startFlow.bot?.command?.text ?? "",
    /Welcome to ${appName.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")}/
  );
  assert.equal(startFlow.miniApp?.route, "/");
  assert.equal(startFlow.steps.home.type, "miniapp");
});
`;
}

