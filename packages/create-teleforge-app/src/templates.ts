  const install = `${options.packageManager} install`;
  const run = `${options.packageManager} run dev`;
  const runPublic = `${options.packageManager} run dev:public`;
  const doctor = `${options.packageManager} run doctor`;

  return `# ${options.appName}

Teleforge starter workspace generated by \`create-teleforge-app\`.

## Quick Start

\`\`\`bash
${install}
cp .env.example .env
${run}
\`\`\`

For a Telegram-openable URL:

\`\`\`bash
${runPublic}
\`\`\`

Check setup health with:

\`\`\`bash
${doctor}
\`\`\`

## Structure

- \`apps/web\`: Mini App surface (${options.mode === "spa" ? "Vite SPA" : "Next.js BFF web"})
- \`apps/bot\`: Bot runtime and command handlers
- \`apps/api\`: optional API and webhook placeholder routes
- \`teleforge.app.json\`: App manifest

## Dev Workflow

- \`${options.packageManager} run dev\`: local browser development with Teleforge's mock bridge and companion bot process
- \`${options.packageManager} run dev:public\`: public HTTPS tunnel for real Telegram testing
- \`${options.packageManager} run doctor\`: manifest and environment diagnostics

## Bot Runtime

- the scaffold runs the bot in polling mode by default
- leave \`MINI_APP_URL\` blank unless you want to force a fixed override
- \`dev\` and \`dev:public\` inject the resolved local or public URL into the companion bot automatically
- the generated \`apps/api\` files are placeholders; webhook mode only makes sense once the primary web runtime actually serves \`/api/webhook\`

## Modes

- \`/\`: available in inline, compact, and fullscreen launch modes
- \`/settings\`: fullscreen-only sample route with additional guard requirements
`;
