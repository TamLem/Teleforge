interface BuildProjectFilesOptions {
  appId: string;
  appName: string;
  botUsername: string;
  /** When set, use `link:` protocol pointing to this local teleforge monorepo path. */
  linkPath?: string;
}

interface PackageNames {
  api: string;
  bot: string;
  types: string;
  web: string;
}

function teleforgeDependency(linkPath?: string): Record<string, string> {
  return {
    teleforge: !linkPath ? "^0.5.0" : `link:${linkPath}/packages/teleforge`
  };
}

export function buildProjectFiles(options: BuildProjectFilesOptions): Record<string, string> {
  const packageNames = getPackageNames(options.appId);

  return {
    ".env.example": envExample(),
    ".gitignore": generatedGitignore(),
    "README.md": generatedReadme(options),
    "package.json": generatedRootPackageJson(options),
    "pnpm-workspace.yaml": 'packages:\n  - "apps/*"\n  - "packages/*"\n',
    "teleforge.config.ts": generatedConfig(options),
    "tsconfig.base.json": generatedBaseTsconfig(),
    ...generatedApiFiles(packageNames),
    "apps/bot/package.json": generatedBotPackageJson(packageNames),
    "apps/bot/src/flows/start.flow.ts": botStartFlowTs(options.appName, packageNames),
    "apps/bot/src/index.ts": botIndexTs(),
    "apps/bot/src/runtime.ts": botRuntimeTs(),
    "apps/bot/test/start.test.ts": botStartTestTs(options.appName),
    "apps/bot/tsconfig.json": botTsconfig(),
    "packages/types/package.json": generatedTypesPackageJson(packageNames),
    "packages/types/src/index.ts": generatedTypesIndexTs(),
    "packages/types/tsconfig.json": typesTsconfig(),
    ...generatedWebFiles(options, packageNames)
  };
}

function getPackageNames(appId: string): PackageNames {
  const scope = `@${appId}`;

  return {
    api: `${scope}/api`,
    bot: `${scope}/bot`,
    types: `${scope}/types`,
    web: `${scope}/web`
  };
}

function generatedApiFiles(packageNames: PackageNames): Record<string, string> {
  return {
    "apps/api/package.json": generatedApiPackageJson(packageNames),
    "apps/api/src/index.ts": apiIndexTs(),
    "apps/api/src/loaders/home.loader.ts": homeLoaderTs(),
    "apps/api/tsconfig.json": apiTsconfig()
  };
}

function generatedReadme(options: BuildProjectFilesOptions): string {
  const install = `pnpm install`;
  const run = `pnpm run dev`;
  const runPublic = `pnpm run dev:public`;
  const doctor = `pnpm run doctor`;
  const runTests = `pnpm test`;
  const generate = `pnpm run generate`;

  return `# ${options.appName}

Teleforge starter workspace generated by \`teleforge create\`.

## Quick Start

\`\`\`bash
${install}
cp .env.example .env
${generate}
${run}
\`\`\`

For a Telegram-openable URL with public HTTPS:

\`\`\`bash
${runPublic}
\`\`\`

Run the baseline scaffold tests with:

\`\`\`bash
${runTests}
\`\`\`

Check setup health with:

\`\`\`bash
${doctor}
\`\`\`

## Structure

- \`apps/web\`: Mini App shell, screens, and styles
- \`apps/bot\`: Discovered flows plus the bot/server runtime
- \`apps/api\`: Server loaders for Mini App screens
- \`packages/types\`: Shared types (generated contracts live in apps/web/src/teleforge-generated/)
- \`teleforge.config.ts\`: App definition

## Read These Files First

- \`teleforge.config.ts\`: source of truth for app identity, flow discovery, and Mini App defaults
- \`apps/bot/src/flows/start.flow.ts\`: the first flow with bot command and actions
- \`apps/api/src/loaders/home.loader.ts\`: server loader for the home screen
- \`apps/web/src/main.tsx\`: the Mini App shell entrypoint
- \`apps/web/src/screens/home.screen.tsx\`: the first Mini App screen with typed props
- \`apps/web/src/teleforge-contract-overrides.ts\`: typed payload and loader data overrides

The scaffold starts with one flow, one screen, one action, and one loader. \`pnpm run generate\` creates:
- \`apps/web/src/teleforge-generated/client-flow-manifest.ts\`
- \`apps/web/src/teleforge-generated/contracts.ts\`

These are refreshed automatically before dev, test, and build.

## Dev Workflow

- \`pnpm run dev\`: local simulator with chat, embedded Mini App, fixtures, and debug panel
- \`pnpm run dev:public\`: public HTTPS tunnel for real Telegram testing
- \`pnpm test\`: baseline bot and screen smoke tests
- \`pnpm run doctor\`: config, manifest, and environment diagnostics
- \`pnpm run generate\`: regenerate client manifest and typed contracts

## Next Steps

Add more flows in \`apps/bot/src/flows\`, more screens in \`apps/web/src/screens\`, and more loaders in \`apps/api/src/loaders\`. See the Teleforge docs for:

- [Runtime Wiring](https://teleforge.dev/docs/runtime-wiring)
- [State Boundaries](https://teleforge.dev/docs/state-boundaries)
- [Generated Mini App Contracts](https://teleforge.dev/docs/generated-miniapp-contracts)
- [Server Loaders](https://teleforge.dev/docs/server-hooks)
`;
}

