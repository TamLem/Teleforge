function botIndexTs(): string {
  return `import path from "node:path";
import { fileURLToPath } from "node:url";

import { createTeleforgeRuntimeContext, startTeleforgeBot, startTeleforgeServer } from "teleforge";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");

const context = await createTeleforgeRuntimeContext({
  cwd: projectRoot
});

const { runtime, stop: stopBot } = await startTeleforgeBot({
  app: context.app,
  cwd: context.cwd,
  flowSecret: context.flowSecret,
  miniAppUrl: context.miniAppUrl,
  sessionManager: context.sessionManager,
  services: context.services
});

const { stop: stopServer } = await startTeleforgeServer({
  app: context.app,
  cwd: context.cwd,
  flowSecret: context.flowSecret,
  miniAppUrl: context.miniAppUrl,
  sessionManager: context.sessionManager,
  services: context.services,
  onChatHandoff: (input) => runtime.handleChatHandoff(input)
});

process.on("SIGINT", () => {
  console.log("\\n[bot] shutting down...");
  stopBot();
  stopServer();
  process.exit(0);
});

process.on("SIGTERM", () => {
  console.log("\\n[bot] shutting down...");
  stopBot();
  stopServer();
  process.exit(0);
});
`;
}

function botRuntimeTs(): string {
  return `import path from "node:path";
import { fileURLToPath } from "node:url";

import { startTeleforgeBot } from "teleforge";
import { type BotRuntime } from "teleforge/bot";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../..");

export async function createDevBotRuntime(options?: { miniAppUrl?: string }): Promise<BotRuntime> {
  const { runtime } = await startTeleforgeBot({
    cwd: projectRoot,
    miniAppUrl: options?.miniAppUrl
  });
  return runtime;
}
`;
}

function botStartFlowTs(appName: string, _packageNames: PackageNames): string {
  return `import { defineFlow } from "teleforge";

export default defineFlow({
  id: "start",
  command: {
    command: "start",
    description: "Start the Mini App",
    handler: async ({ ctx, sign }) => {
      const url = await sign({
        screenId: "home",
        subject: { resource: { type: "welcome" } },
        allowedActions: ["acknowledge"]
      });

      await ctx.reply("Welcome to ${appName}. Open the Mini App to continue.", {
        reply_markup: {
          inline_keyboard: [[{ text: "Open App", web_app: { url } }]]
        }
      });
    }
  },
  miniApp: {
    routes: { "/": "home" },
    defaultRoute: "/",
    title: "${appName}"
  },
  actions: {
    acknowledge: {
      handler: async () => {
        return { data: { acknowledged: true } };
      }
    }
  }
});
`;
}

